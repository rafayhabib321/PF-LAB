#include <iostream>
using namespace std;

int main() {
    int n = 5; // Number of rows for half of the diamond
    // Upper half of the diamond (pyramid)
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= n - i; j++) {
            cout << " ";
        }
        for (int k = 1; k <= (2 * i - 1); k++) {
            cout << "*";
        }
        cout << endl;
    }
    // Lower half of the diamond (inverted pyramid)
    for (int i = n - 1; i >= 1; i--) {
        for (int j = 1; j <= n - i; j++) {
            cout << " ";
        }
        for (int k = 1; k <= (2 * i - 1); k++) {
            cout << "*";
        }
        cout << endl;
    }
    cout<<'\n';
    cout<<"Diamond.";
    return 0;
}
